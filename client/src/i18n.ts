const DICTS: Record<Lang, Partial<Record<TKey, string>>> = { en, si, ta }

function fmt(s: string, vars?: Record<string, string | number>): string {
  if (!vars) return s
  let out = s
  for (const k of Object.keys(vars)) out = out.split(`{${k}}`).join(String(vars[k]))
  return out
}

/** Pure translate (usable outside React, e.g. in the state hook). */
export function tr(lang: Lang, key: TKey, vars?: Record<string, string | number>): string {
  return fmt(DICTS[lang][key] ?? en[key], vars)
}

export const LangContext = createContext<Lang>('en')

export function useT() {
  const lang = useContext(LangContext)
  return (key: TKey, vars?: Record<string, string | number>) => tr(lang, key, vars)
}

export type TFunc = ReturnType<typeof useT>
